package CLG;

public class patternK0 {
    public static void main(String[] args){

        // PATTERN 1 , RIGHT ANGLE TRIANGLE

        for(int i =0; i<=4; i++){
            System.out.println();
            for(int j=i; j>=0; j--){
                System.out.print("* ");
            }
        }
    }
    
}
