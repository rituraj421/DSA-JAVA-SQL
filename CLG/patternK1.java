public class patternK1 {
    public static void main(String[] args){

        // PATTETRN 2 , INVERSE RIGHT ANGLE TRIANGLE
        for(int i = 0; i<=4; i++){
            System.out.println();
            for(int j = i; j<=4; j++){
                System.out.print("* ");
            }
        }
    }
}
