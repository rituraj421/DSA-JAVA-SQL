package com.company;

public class Ch5_DoWhileLoopV22 {
    public static void main(String[] args) {
        int a = 0;
        while(a<5){
            System.out.println(a);
            a++;
        }
        int b = 0;
        do{
            System.out.println("This is question 2");
            System.out.println(b);
            b++;
        }while(b<=5);
    }
}
